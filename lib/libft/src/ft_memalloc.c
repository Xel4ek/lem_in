#include "libft.h"
#include <stdlib.h>

void	*ft_memalloc(size_t size)
{
	void *ptr;

	if (!(ptr = (void*)malloc(size)) || !size)
		return (NULL);
	ft_bzero(ptr, size);
	return (ptr);
}
